const paths = openapiJson.paths;
function descriptionToIdentifier(description, method = "") {
    const prefix = description
        .toLocaleLowerCase("en-us")
        .startsWith(method.toLocaleLowerCase("en-us"))
        ? ""
        : method.charAt(0).toUpperCase() + method.slice(1);
    return (prefix +
        description
            .split(/[^\w]+/g)
            .slice(0, 10)
            .map((part) => part.charAt(0).toUpperCase() + part.slice(1))
            .join(""));
}
const apiTypesOutputPath = path.join(dir, `api.types.ts`);
const apiTypesWriteStream = fs.createWriteStream(apiTypesOutputPath);
apiTypesWriteStream.write(`/** This file is auto-generated by ${path.basename(fileURLToPath(import.meta.url))}. Do not edit directly. */\n`);
apiTypesWriteStream.write(`import type * as openapi from "./schemas.ts";\n\n`);
const createRequestInitOverrides = [];
for (const urlPath of Object.keys(paths)) {
    const pathDef = paths[urlPath];
    for (const method of Object.keys(pathDef)) {
        const exportStatements = [];
        const methodStr = `${method}`;
        const methodDef = pathDef[method];
        let queryParamsDef = null;
        let pathParamsDef = null;
        let requestBodyDef = null;
        const prefix = descriptionToIdentifier(methodDef.description, methodStr);
        if (methodDef.parameters) {
            const queryParameters = methodDef.parameters.filter((param) => "in" in param && param.in === "query");
            const pathParameters = methodDef.parameters.filter((param) => "in" in param && param.in === "path");
            if (queryParameters.length) {
                const def = `${prefix}QueryParams`;
                const params = queryParameters.map((param) => {
                    const paramObj = param;
                    const name = paramObj.name;
                    const type = buildType(paramObj.schema, undefined, 0, "openapi");
                    return `  ${name}: ${type};`;
                });
                const exportStatement = `export type ${def} = {\n${params.join("\n")}\n}`;
                exportStatements.push(exportStatement);
                queryParamsDef = def;
            }
            if (pathParameters.length) {
                const def = `${prefix}PathParams`;
                const params = pathParameters.map((param) => {
                    const paramObj = param;
                    const name = paramObj.name;
                    const type = buildType(paramObj.schema, undefined, 0, "openapi");
                    return `  ${name}: ${type};`;
                });
                const exportStatement = `export type ${def} = {\n${params.join("\n")}\n}`;
                exportStatements.push(exportStatement);
                pathParamsDef = def;
            }
        }
        if (methodDef.requestBody) {
            const def = `${prefix}RequestBody`;
            if (!("content" in methodDef.requestBody)) {
                continue;
            }
            const content = methodDef.requestBody.content;
            if (!("application/json" in content)) {
                continue;
            }
            const jsonContent = content["application/json"];
            const type = buildType(jsonContent.schema, undefined, 0, "openapi");
            const exportStatement = `export type ${def} = ${type}`;
            exportStatements.push(exportStatement);
            requestBodyDef = def;
        }
        const responseTypes = [];
        for (const _responseCode of Object.keys(methodDef.responses)) {
            const responseCode = Number(_responseCode);
            const responseDef = methodDef.responses[responseCode];
            if (!responseDef.content) {
                continue;
            }
            const def = `${prefix}Response${responseCode}`;
            const content = responseDef.content;
            if (!("application/json" in content)) {
                continue;
            }
            const jsonContent = content["application/json"];
            const type = buildType(jsonContent.schema, undefined, 0, "openapi");
            const exportStatement = `export type ${def} = ${type}`;
            exportStatements.push(exportStatement);
            responseTypes.push([responseCode, def]);
        }
        const responseDef = `${prefix}Response`;
        const exportResponseStatement = `export type ${responseDef} = [${responseTypes.map(([code, def]) => `[${code}, ${def}]`).join(", ")}]`;
        exportStatements.push(exportResponseStatement);
        apiTypesWriteStream.write(`\n${exportStatements.join("\n\n")}\n`);
        const exportRequestResponseStatement = [
            `export interface ${prefix}API {`,
            `  id: "${prefix}";`,
            `  method: "${methodStr}";`,
            `  query: ${queryParamsDef ?? "null"};`,
            `  pathParams: ${pathParamsDef ?? "null"};`,
            `  requestBody: ${requestBodyDef ?? "null"};`,
            `  response: ${responseDef};`,
            `}`,
        ].join("\n");
        apiTypesWriteStream.write(`\n${exportRequestResponseStatement}\n`);
        createRequestInitOverrides.push({
            id: prefix,
            method: methodStr,
            path: urlPath,
            queryParamsDef,
            pathParamsDef,
            requestBodyDef,
        });
    }
}
