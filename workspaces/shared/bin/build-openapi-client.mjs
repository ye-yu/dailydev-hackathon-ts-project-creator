function buildType(schema, parent, indent = 0, importSpecifier) {
    if ("$ref" in schema) {
        const ref = `${schema.$ref}`;
        if (!ref.startsWith("#/")) {
            throw new Error(`Expected $ref to start with "#/", but got ${ref}`, {
                cause: { schema, parent },
            });
        }
        const refPath = ref.slice(2).split("/");
        let refSchema = openapiJson;
        for (const segment of refPath) {
            if (!(segment in refSchema)) {
                throw new Error(`Invalid $ref path: ${ref}, segment "${segment}" not found in schema`, {
                    cause: { schema, parent },
                });
            }
            if (typeof refSchema !== "object" || refSchema === null) {
                throw new Error(`Invalid $ref path: ${ref}, segment "${segment}" does not point to an object`, { cause: { schema, parent } });
            }
            const segmentContent = Reflect.get(refSchema, segment);
            refSchema = segmentContent;
        }
        if (!("title" in refSchema)) {
            throw new Error(`Expected referenced schema to have a "title" property, but got ${JSON.stringify(refSchema)}`, { cause: { schema, parent } });
        }
        const nullable = "nullable" in schema && schema.nullable ? " | null" : "";
        const prefix = importSpecifier ? `${importSpecifier}.` : "";
        return `${prefix}${refSchema.title}${nullable}`;
    }
    if (!("type" in schema)) {
        throw new Error(`Expected schema to have a "type" property, but got ${JSON.stringify(schema)}`, { cause: { schema, parent } });
    }
    const type = `${schema.type}`;
    switch (type) {
        case "object": {
            if (!("properties" in schema)) {
                throw new Error(`Expected object schema to have a "properties" property, but got ${JSON.stringify(schema)}`, { cause: { schema, parent } });
            }
            const properties = schema.properties;
            const result = {};
            for (const key of Object.keys(properties)) {
                try {
                    result[key] = buildType(properties[key], parent ?? schema, indent + 2, importSpecifier);
                }
                catch (error) {
                    throw new Error(`Error processing property "${key}": ${error.message}`, {
                        cause: { schema: properties[key], parent: schema, error },
                    });
                }
            }
            if (!("title" in schema)) {
                return `{\n${Object.entries(result)
                    .map(([key, value]) => `${" ".repeat(indent + 2)}${key}: ${value};`)
                    .join("\n")}\n${" ".repeat(indent)}}`;
            }
            const title = schema.title;
            return `interface ${title} {\n${Object.entries(result)
                .map(([key, value]) => `${" ".repeat(indent + 2)}${key}: ${value};`)
                .join("\n")}\n${" ".repeat(indent)}}`;
        }
        case "array": {
            if (!("items" in schema)) {
                throw new Error(`Expected array schema to have an "items" property, but got ${JSON.stringify(schema)}`, { cause: { schema, parent } });
            }
            const items = schema.items;
            try {
                return `Array<${buildType(items, parent ?? schema, indent + 2, importSpecifier)}>`;
            }
            catch (error) {
                throw new Error(`Error processing items of array schema: ${error.message}`, {
                    cause: { schema: items, parent: schema, error },
                });
            }
        }
        case "string": {
            const nullable = "nullable" in schema && schema.nullable ? " | null" : "";
            return `string${nullable}`;
        }
        case "number":
        case "integer": {
            const nullable = "nullable" in schema && schema.nullable ? " | null" : "";
            return `number${nullable}`;
        }
        case "boolean": {
            const nullable = "nullable" in schema && schema.nullable ? " | null" : "";
            return `boolean${nullable}`;
        }
        default: {
            throw new Error(`Unsupported schema type: ${type}`, { cause: { schema, parent } });
        }
    }
}
const schemas = openapiJson.components.schemas;
const schemaExports = Object.keys(schemas)
    .map((key) => {
    const schemaKey = key;
    const schema = schemas[schemaKey];
    return `export ${buildType(schema)}`;
})
    .join("\n\n");
